def get_min_max(data: list) -> tuple[int]|None:
    if data:
        _min, _max = float('inf'), float('-inf')
        i, i_min, i_max = [0] * 3
        for el in data:
            if _min > el:
                _min, i_min = el, i
            if _max < el:
                _max, i_max = el, i
            i += 1  
        return (i_min, i_max)
    return None

