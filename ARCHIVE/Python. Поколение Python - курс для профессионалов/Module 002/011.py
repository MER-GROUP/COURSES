def get_biggest(numbers: list) -> int:
    if (0 == len(numbers)):
        return -1
    else:
        max_num = max(numbers)
        max_len_num = len(str(max_num))
        arr_str = list(map(str, numbers))
        arr_res = sorted(
            arr_str,
            reverse=True,
            key=lambda x: x * max_len_num
        )
        # print(arr_res)
        return ''.join(arr_res) if int(''.join(arr_res)) else 0

