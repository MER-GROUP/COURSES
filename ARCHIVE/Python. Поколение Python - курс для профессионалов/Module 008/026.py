def dict_travel(nested_dicts: dict) -> None:
    # _dict = dict()
    def rec(ndict: dict = nested_dicts, string: str = '') -> None:
        for k, v in sorted(ndict.items()):
            if isinstance(v, dict):
                rec(v, string+f'{k}.')
            else:
                # _dict[string+f'{k}'] = v
                print(string+f'{k}: {v}')
    rec(nested_dicts, '')
