from functools import reduce
import operator
file = open('prices.txt', 'rt', encoding='utf-8')

print(
    sum(
        map(
            lambda x: reduce(operator.mul, x),
            map(
                lambda x: map(int, x),
                map(
                    lambda x: x.split()[1 : 3],
                    file
                )
            )
        )  
    )
)

file.close()