# Рациональное число
# Рациональное число – это число, которое можно представить в виде дроби n/m, где n и m соответственно, числитель и знаменатель, которые имеют целочисленное значение, при этом знаменатель не равен нулю. 
# Например, в дроби 5/6 числитель m = 5, а знаменатель n=6.
# Знаменатель дроби показывает количество равных частей, а числитель дроби показывает, сколько из них взято.