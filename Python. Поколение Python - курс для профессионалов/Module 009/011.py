def new_sort(s: str) -> str:
    # print(list(s)) # test
    res = sorted(s, key=lambda x: (
            x.isdigit(),
            x.isdigit() and not int(x)%2,
            x.isupper(),
            x.lower()           
        )
    )
    # print(res) # test
    return "".join(res)

