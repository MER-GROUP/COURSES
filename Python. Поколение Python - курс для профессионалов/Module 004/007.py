# )

arr = tuple(
    [line.strip().split(' / ') for line in sys.stdin]
)

res = sorted(
    filter(
        lambda x: [x[1]] == arr[-1],
        arr[:-1]
    ),
    key=lambda x: (float(x[2]), x[0])
)

for i in res:
    print(i[0])